RowColToCell    PROC ;al = row  cl = col  =>> si = CellNumber
                push ax

                shl al, 3  ; al = al*8
                add al, cl ; al = al*8 + col

                sub ah, ah
                mov si, ax


                pop ax
                RET
ENDP RowColToCell

